package programmingConstructs;

public class usingWhileLoop {
    /*
     *her ewe use while loop to print squares of number from 1 to 100.
     */
    public static void main(String[] args) {
        int i = 1;
        while (i <= 100) {
            System.out.println(i + "^2 =" + i * i);
            i++;
        }
    }
}
